"""Question 27: Determine whether a number is positive, negative, or zero."""  # Executes this statement.


def main() -> None:  # Defines a function.
    number = 0  # Stores a value in variable(s).
    if number > 0:  # Checks a condition.
        print("Positive")  # Displays output to the user.
    elif number < 0:  # Checks another condition if needed.
        print("Negative")  # Displays output to the user.
    else:  # Runs when previous conditions are false.
        print("Zero")  # Displays output to the user.


if __name__ == "__main__":  # Checks a condition.
    main()  # Executes this statement.
