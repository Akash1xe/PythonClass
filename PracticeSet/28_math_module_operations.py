"""Question 28: Import math module and perform operations."""  # Executes this statement.


import math  # Imports required module(s).


def main() -> None:  # Defines a function.
    value = 16  # Stores a value in variable(s).
    print("Square root:", math.sqrt(value))  # Displays output to the user.
    print("Power:", math.pow(2, 5))  # Displays output to the user.
    print("Logarithm:", math.log(10))  # Displays output to the user.
    print("Factorial:", math.factorial(5))  # Displays output to the user.


if __name__ == "__main__":  # Checks a condition.
    main()  # Executes this statement.
