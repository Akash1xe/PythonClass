"""Question 11: Sets in Python, creation, and properties."""  # Executes this statement.


def main() -> None:  # Defines a function.
    numbers = {1, 2, 3, 3, 2}  # Stores a value in variable(s).
    empty_set = set()  # Stores a value in variable(s).

    print("Set:", numbers)  # Displays output to the user.
    print("Empty set type:", type(empty_set))  # Displays output to the user.
    print("Properties:")  # Displays output to the user.
    print("- Unordered")  # Displays output to the user.
    print("- Unique elements only")  # Displays output to the user.
    print("- Mutable")  # Displays output to the user.
    print("- No indexing")  # Displays output to the user.


if __name__ == "__main__":  # Checks a condition.
    main()  # Executes this statement.
